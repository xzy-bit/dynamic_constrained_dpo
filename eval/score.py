# eval/score.py
import argparse
import json
import os
from typing import List, Dict, Any, Iterable, Tuple

import torch
from transformers import AutoTokenizer, AutoModelForSequenceClassification
from tqdm import tqdm


def default_out_path(in_path: str) -> str:
    if in_path.endswith(".json"):
        return in_path[:-5] + "-reward.json"
    return in_path + "-reward.json"


def build_text(tokenizer, instruction: str, response: str) -> str:
    # Prefer chat template if available (Llama3-style RM usually expects chat format)
    if hasattr(tokenizer, "apply_chat_template") and getattr(tokenizer, "chat_template", None):
        messages = [
            {"role": "user", "content": instruction},
            {"role": "assistant", "content": response},
        ]
        return tokenizer.apply_chat_template(
            messages, tokenize=False, add_generation_prompt=False
        )
    # Fallback: plain format
    return f"### Instruction:\n{instruction}\n\n### Response:\n{response}\n"


def iter_pairs(
    data: List[Dict[str, Any]],
    tokenizer,
    first_only: bool = False,
) -> Iterable[Tuple[int, int, str]]:
    """
    Yield (sample_idx, cand_idx, text_for_rm)
    """
    for si, ex in enumerate(data):
        instr = ex.get("instruction", "")
        outs = ex.get("outputs", None)
        if not isinstance(outs, list):
            raise ValueError(f"Example {si} missing 'outputs' list.")
        if first_only:
            outs = outs[:1]
        for cj, resp in enumerate(outs):
            yield si, cj, build_text(tokenizer, instr, str(resp))


@torch.inference_mode()
def score_stream(
    model,
    tokenizer,
    data: List[Dict[str, Any]],
    batch_size: int,
    max_length: int,
    first_only: bool = False,
) -> None:
    """
    In-place write ex["reward"] as list[float] aligned with ex["outputs"].
    Streaming to avoid materializing flat_texts (huge CPU/memory).
    """
    # pre-allocate reward lists
    for ex in data:
        outs = ex.get("outputs", [])
        if first_only:
            ex["outputs"] = outs[:1]
            outs = ex["outputs"]
        reward_len = min(len(outs), 1) if first_only else len(outs)
        ex["reward"] = [None] * reward_len

    # total candidates for progress bar
    if first_only:
        total = sum(min(len(ex["outputs"]), 1) for ex in data)
    else:
        total = sum(len(ex["outputs"]) for ex in data)

    buf_texts: List[str] = []
    buf_indices: List[Tuple[int, int]] = []

    pbar = tqdm(total=total, desc="Scoring (RM)", unit="cand")

    for si, cj, text in iter_pairs(data, tokenizer, first_only=first_only):
        buf_texts.append(text)
        buf_indices.append((si, cj))

        if len(buf_texts) >= batch_size:
            enc = tokenizer(
                buf_texts,
                padding=True,
                truncation=True,
                max_length=max_length,  # keep 4096 as you require
                return_tensors="pt",
            )
            out = model(**enc)
            logits = out.logits
            batch_scores = logits.squeeze(-1) if (logits.dim() == 2 and logits.size(-1) == 1) else logits.squeeze()

            scores = batch_scores.float().detach().cpu().tolist()
            for (ssi, ccj), sc in zip(buf_indices, scores):
                data[ssi]["reward"][ccj] = float(sc)

            pbar.update(len(buf_texts))
            buf_texts.clear()
            buf_indices.clear()

    # flush last partial batch
    if buf_texts:
        enc = tokenizer(
            buf_texts,
            padding=True,
            truncation=True,
            max_length=max_length,
            return_tensors="pt",
        )
        out = model(**enc)
        logits = out.logits
        batch_scores = logits.squeeze(-1) if (logits.dim() == 2 and logits.size(-1) == 1) else logits.squeeze()

        scores = batch_scores.float().detach().cpu().tolist()
        for (ssi, ccj), sc in zip(buf_indices, scores):
            data[ssi]["reward"][ccj] = float(sc)

        pbar.update(len(buf_texts))

    pbar.close()

    # sanity check: no None left
    for si, ex in enumerate(data):
        if any(v is None for v in ex["reward"]):
            raise RuntimeError(f"Missing rewards at sample {si}: found None entries.")


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--input_json", type=str, required=True, help="Path to xxx.json generated by gen.py")
    ap.add_argument("--rm_name_or_path", type=str, default="sfairXC/FsfairX-LLaMA3-RM-v0.1")
    ap.add_argument("--tokenizer_name_or_path", type=str, default=None, help="Tokenizer name/path (default=rm)")
    ap.add_argument("--batch_size", type=int, default=64, help="Try 32/64/128; 4096 ctx is heavy.")
    ap.add_argument("--max_length", type=int, default=4096, help="Max tokens for RM input")
    ap.add_argument("--dtype", type=str, default="bfloat16", choices=["float16", "bfloat16", "float32"])
    ap.add_argument("--output_json", type=str, default=None, help="Output path (default=xxx-reward.json)")
    ap.add_argument(
        "--first_only",
        action="store_true",
        help="Only score outputs[0] for each example and write a length-1 reward list.",
    )
    args = ap.parse_args()

    out_path = args.output_json or default_out_path(args.input_json)

    with open(args.input_json, "r", encoding="utf-8") as f:
        data: List[Dict[str, Any]] = json.load(f)

    tok_name = args.tokenizer_name_or_path or args.rm_name_or_path
    print(f"Loading tokenizer: {tok_name}")
    tokenizer = AutoTokenizer.from_pretrained(tok_name, trust_remote_code=True)
    if tokenizer.pad_token is None and tokenizer.eos_token is not None:
        tokenizer.pad_token = tokenizer.eos_token

    print(f"Loading reward model: {args.rm_name_or_path}")
    torch_dtype = {"float16": torch.float16, "bfloat16": torch.bfloat16, "float32": torch.float32}[args.dtype]

    # Keep your hardware/distributed behavior: device_map="auto"
    # (No manual model.to(cuda) and no enc.to(cuda) to avoid device mismatch.)
    model = AutoModelForSequenceClassification.from_pretrained(
        args.rm_name_or_path,
        trust_remote_code=True,
        torch_dtype=torch_dtype,
        device_map="auto",
        # Optional small speedup if available; safe to keep commented if your env is uncertain
        # attn_implementation="sdpa",
    )
    model.eval()

    if args.first_only:
        total = sum(min(len(ex.get("outputs", [])), 1) for ex in data)
    else:
        total = sum(len(ex.get("outputs", [])) for ex in data)
    print(f"Scoring {total} candidate responses...")

    score_stream(
        model=model,
        tokenizer=tokenizer,
        data=data,
        batch_size=args.batch_size,
        max_length=args.max_length,  # keep 4096
        first_only=args.first_only,
    )

    os.makedirs(os.path.dirname(out_path) or ".", exist_ok=True)
    with open(out_path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"Done. Saved to: {out_path}")


if __name__ == "__main__":
    main()
